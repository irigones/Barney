lista = [2, 3, 7, 12, 2]

print("lista de números")
for numero in lista:
    print(numero)

# Multiplicar cada número por 2
lista_multiplicada = []
for numero in lista:
    lista_multiplicada.append(numero * 2)

# Imprimir os números multiplicados por 2
print("lista de números x 2")
for numero in lista_multiplicada:
    print(numero)